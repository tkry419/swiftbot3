/**
 * SwiftBot - plugins/commands/automation/antispam.js
 * Anti Spam Manager - Full Control
 * Delete spam, warn, kick - Owner only
 */

export default {
  name: 'antispam',
  alias: ['nospam', 'antiflood', 'spam', 'as'],
  desc: 'Control anti spam protection in groups',
  usage: '[on/off/status] [global/group] [limit] [time]',
  category: 'Automation',
  permission: 'owner',

  execute: async (sock, m, args, { db, prefix, isOwner, isGroup }) => {
    const from = m.key.remoteJid

    if (!isOwner) {
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Owner only command\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    const action = args[0]?.toLowerCase()
    const target = args[1]?.toLowerCase()
    const value = args[2]

    // STATUS
    if (!action || action === 'status' || action === 'info') {
      const [
        antispam, action_type, limit, window,
        groups, groupsEnabled, whitelist
      ] = await Promise.all([
        db.get('antispam'),
        db.get('antispamAction'),
        db.get('antispamLimit'),
        db.get('antispamWindow'),
        db.get('antispamGroups'),
        db.get('antispamGroupsEnabled'),
        db.get('antispamWhitelist')
      ])

      const groupList = groups?.length? groups.map(g => g.split('@')[0]).join(', ') : 'None'
      const whitelistList = whitelist?.length? whitelist.map(u => u.split('@')[0]).join(', ') : 'None'

      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’
в•‘ рџљ« ANTI SPAM STATUS
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ Status: ${antispam? 'рџџұ ON' : 'рџ”ө OFF'}
в•‘ Action: ${action_type || 'delete'}
в•‘ Limit: ${limit || 5} msgs
в•‘ Window: ${window || 10}s
в•‘
в•‘ рџ“Қ TARGETS:
в•‘ All Groups: ${groupsEnabled!== false? 'вњ… ON' : 'вқЊ OFF'}
в•‘ Specific Groups: ${groups?.length || 0}
в•‘ ${groups?.length? 'р– Ѓ ' + groupList : ''}
в•‘ Whitelisted Users: ${whitelist?.length || 0}
в•‘ ${whitelist?.length? 'р– Ѓ ' + whitelistList : ''}
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ рџ“қ USAGE:
в•‘ ${prefix}as on global
в•‘ ${prefix}as on group
в•‘ ${prefix}as off global
в•‘ ${prefix}as action delete
в•‘ ${prefix}as action warn
в•‘ ${prefix}as action kick
в•‘ ${prefix}as limit 5
в•‘ ${prefix}as time 10
в•‘ ${prefix}as add whitelist 255xxx
в•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // ON / OFF
    if (action === 'on' || action === 'enable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antispam', true)
        await db.set('antispamGroupsEnabled', true)
        await db.set('antispamAction', 'delete')
        await db.set('antispamLimit', 5)
        await db.set('antispamWindow', 10)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… Anti Spam Enabled\nв•‘ Mode: GLOBAL\nв•‘ Limit: 5 msgs/10s\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        const groups = await db.get('antispamGroups') || []
        if (!groups.includes(from)) {
          groups.push(from)
          await db.set('antispamGroups', groups)
        }
        await db.set('antispam', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… Anti Spam Enabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // OFF / DISABLE
    if (action === 'off' || action === 'disable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antispam', false)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Anti Spam Disabled\nв•‘ Mode: GLOBAL OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        let groups = await db.get('antispamGroups') || []
        groups = groups.filter(g => g!== from)
        await db.set('antispamGroups', groups)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Anti Spam Disabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // ACTION TYPE
    if (action === 'action' || action === 'punish' || action === 'set') {
      if (['delete', 'warn', 'kick'].includes(target)) {
        await db.set('antispamAction', target)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вљ™пёЏ Action Set\nв•‘ Type: ${target.toUpperCase()}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // LIMIT
    if (action === 'limit') {
      const newLimit = parseInt(target)
      if (isNaN(newLimit) || newLimit < 2 || newLimit > 20) {
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Invalid limit\nв•‘ Range: 2-20 msgs\nв•‘ Example: ${prefix}as limit 5\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
      await db.set('antispamLimit', newLimit)
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вљ™пёЏ Limit Set\nв•‘ Messages: ${newLimit}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // TIME WINDOW
    if (action === 'time' || action === 'window') {
      const newWindow = parseInt(target)
      if (isNaN(newWindow) || newWindow < 3 || newWindow > 60) {
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Invalid window\nв•‘ Range: 3-60 seconds\nв•‘ Example: ${prefix}as time 10\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
      await db.set('antispamWindow', newWindow)
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вЏ±пёЏ Time Window Set\nв•‘ Duration: ${newWindow}s\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // ADD WHITELIST
    if (action === 'add') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        const whitelist = await db.get('antispamWhitelist') || []
        if (!whitelist.includes(userJid)) {
          whitelist.push(userJid)
          await db.set('antispamWhitelist', whitelist)
        }
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… User Whitelisted\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // DEL / REMOVE
    if (action === 'del' || action === 'remove' || action === 'delete') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        let whitelist = await db.get('antispamWhitelist') || []
        whitelist = whitelist.filter(u => u!== userJid)
        await db.set('antispamWhitelist', whitelist)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ рџ—‘пёЏ User Removed\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // CLEAR ALL
    if (action === 'clear' || action === 'reset') {
      await db.set('antispamWhitelist', [])
      await db.set('antispamGroups', [])
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ рџ—‘пёЏ All Lists Cleared\nв•‘ Whitelist + Groups reset\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // INVALID
    await sock.sendMessage(from, {
      text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Invalid command\nв•‘ Use: ${prefix}as status\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
    }, { quoted: m })
  }
}