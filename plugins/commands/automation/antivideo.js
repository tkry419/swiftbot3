/**
 * SwiftBot - plugins/commands/automation/antivideo.js
 * Anti Video Manager - Full Control
 * Delete videos, warn, kick - Owner only
 */

export default {
  name: 'antivideo',
  alias: ['novideo', 'antivid', 'novids', 'av'],
  desc: 'Control anti video protection in groups',
  usage: '[on/off/status] [global/group] [action]',
  category: 'Automation',
  permission: 'owner',

  execute: async (sock, m, args, { db, prefix, isOwner, isGroup }) => {
    const from = m.key.remoteJid

    if (!isOwner) {
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Owner only command\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    const action = args[0]?.toLowerCase()
    const target = args[1]?.toLowerCase()
    const value = args[2]

    // STATUS
    if (!action || action === 'status' || action === 'info') {
      const [
        antivideo, action_type, groups, groupsEnabled, whitelist
      ] = await Promise.all([
        db.get('antivideo'),
        db.get('antivideoAction'),
        db.get('antivideoGroups'),
        db.get('antivideoGroupsEnabled'),
        db.get('antivideoWhitelist')
      ])

      const groupList = groups?.length? groups.map(g => g.split('@')[0]).join(', ') : 'None'
      const whitelistList = whitelist?.length? whitelist.map(u => u.split('@')[0]).join(', ') : 'None'

      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’
в•‘ рџЋҐ ANTI VIDEO STATUS
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ Status: ${antivideo? 'рџџў ON' : 'рџ”ґ OFF'}
в•‘ Action: ${action_type || 'delete'}
в•‘
в•‘ рџ“Ќ TARGETS:
в•‘ All Groups: ${groupsEnabled!== false? 'вњ… ON' : 'вќЊ OFF'}
в•‘ Specific Groups: ${groups?.length || 0}
в•‘ ${groups?.length? 'р– Ѓ ' + groupList : ''}
в•‘ Whitelisted Users: ${whitelist?.length || 0}
в•‘ ${whitelist?.length? 'р– Ѓ ' + whitelistList : ''}
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ рџ“ќ USAGE:
в•‘ ${prefix}av on global
в•‘ ${prefix}av on group
в•‘ ${prefix}av off global
в•‘ ${prefix}av action delete
в•‘ ${prefix}av action warn
в•‘ ${prefix}av action kick
в•‘ ${prefix}av add whitelist 255xxx
в•‘ ${prefix}av del whitelist 255xxx
в•‘ ${prefix}av clear
в•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    // ON / OFF
    if (action === 'on' || action === 'enable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antivideo', true)
        await db.set('antivideoGroupsEnabled', true)
        await db.set('antivideoAction', 'delete')
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Anti Video Enabled\nв•‘ Mode: GLOBAL\nв•‘ Action: Delete\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        const groups = await db.get('antivideoGroups') || []
        if (!groups.includes(from)) {
          groups.push(from)
          await db.set('antivideoGroups', groups)
        }
        await db.set('antivideo', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Anti Video Enabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // OFF / DISABLE
    if (action === 'off' || action === 'disable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antivideo', false)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Anti Video Disabled\nв•‘ Mode: GLOBAL OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        let groups = await db.get('antivideoGroups') || []
        groups = groups.filter(g => g!== from)
        await db.set('antivideoGroups', groups)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Anti Video Disabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // ACTION TYPE
    if (action === 'action' || action === 'punish' || action === 'set') {
      if (['delete', 'warn', 'kick'].includes(target)) {
        await db.set('antivideoAction', target)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вљ™пёЏ Action Set\nв•‘ Type: ${target.toUpperCase()}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // ADD WHITELIST
    if (action === 'add') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        const whitelist = await db.get('antivideoWhitelist') || []
        if (!whitelist.includes(userJid)) {
          whitelist.push(userJid)
          await db.set('antivideoWhitelist', whitelist)
        }
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… User Whitelisted\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // DEL / REMOVE
    if (action === 'del' || action === 'remove' || action === 'delete') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        let whitelist = await db.get('antivideoWhitelist') || []
        whitelist = whitelist.filter(u => u!== userJid)
        await db.set('antivideoWhitelist', whitelist)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ рџ—‘пёЏ User Removed\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // CLEAR ALL
    if (action === 'clear' || action === 'reset') {
      await db.set('antivideoWhitelist', [])
      await db.set('antivideoGroups', [])
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ рџ—‘пёЏ All Lists Cleared\nв•‘ Whitelist + Groups reset\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    // INVALID
    await sock.sendMessage(from, {
      text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Invalid command\nв•‘ Use: ${prefix}av status\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
    }, { quoted: m })
  }
}