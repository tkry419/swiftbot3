/**
 * SwiftBot - plugins/commands/automation/antisticker.js
 * Anti Sticker Manager - Full Control
 * Delete stickers, warn, kick - Owner only
 */

export default {
  name: 'antisticker',
  alias: ['nosticker', 'antistik', 'nostik', 'ast'],
  desc: 'Control anti sticker protection in groups',
  usage: '[on/off/status] [global/group] [action]',
  category: 'Automation',
  permission: 'owner',

  execute: async (sock, m, args, { db, prefix, isOwner, isGroup }) => {
    const from = m.key.remoteJid

    if (!isOwner) {
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Owner only command\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    const action = args[0]?.toLowerCase()
    const target = args[1]?.toLowerCase()
    const value = args[2]

    // STATUS
    if (!action || action === 'status' || action === 'info') {
      const [
        antisticker, action_type, groups, groupsEnabled, whitelist
      ] = await Promise.all([
        db.get('antisticker'),
        db.get('antistickerAction'),
        db.get('antistickerGroups'),
        db.get('antistickerGroupsEnabled'),
        db.get('antistickerWhitelist')
      ])

      const groupList = groups?.length? groups.map(g => g.split('@')[0]).join(', ') : 'None'
      const whitelistList = whitelist?.length? whitelist.map(u => u.split('@')[0]).join(', ') : 'None'

      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’
в•‘ рџљ« ANTI STICKER STATUS
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ Status: ${antisticker? 'рџџұ ON' : 'рџ”ө OFF'}
в•‘ Action: ${action_type || 'delete'}
в•‘
в•‘ рџ“Қ TARGETS:
в•‘ All Groups: ${groupsEnabled!== false? 'вњ… ON' : 'вқЊ OFF'}
в•‘ Specific Groups: ${groups?.length || 0}
в•‘ ${groups?.length? 'р– Ѓ ' + groupList : ''}
в•‘ Whitelisted Users: ${whitelist?.length || 0}
в•‘ ${whitelist?.length? 'р– Ѓ ' + whitelistList : ''}
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ рџ“қ USAGE:
в•‘ ${prefix}ast on global
в•‘ ${prefix}ast on group
в•‘ ${prefix}ast off global
в•‘ ${prefix}ast action delete
в•‘ ${prefix}ast action warn
в•‘ ${prefix}ast action kick
в•‘ ${prefix}ast add whitelist 255xxx
в•‘ ${prefix}ast del whitelist 255xxx
в•‘ ${prefix}ast clear
в•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // ON / OFF
    if (action === 'on' || action === 'enable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antisticker', true)
        await db.set('antistickerGroupsEnabled', true)
        await db.set('antistickerAction', 'delete')
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… Anti Sticker Enabled\nв•‘ Mode: GLOBAL\nв•‘ Action: Delete\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        const groups = await db.get('antistickerGroups') || []
        if (!groups.includes(from)) {
          groups.push(from)
          await db.set('antistickerGroups', groups)
        }
        await db.set('antisticker', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… Anti Sticker Enabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // OFF / DISABLE
    if (action === 'off' || action === 'disable') {
      if (!target || target === 'global' || target === 'all') {
        await db.set('antisticker', false)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Anti Sticker Disabled\nв•‘ Mode: GLOBAL OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        let groups = await db.get('antistickerGroups') || []
        groups = groups.filter(g => g!== from)
        await db.set('antistickerGroups', groups)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Anti Sticker Disabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // ACTION TYPE
    if (action === 'action' || action === 'punish' || action === 'set') {
      if (['delete', 'warn', 'kick'].includes(target)) {
        await db.set('antistickerAction', target)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вљ™пёЏ Action Set\nв•‘ Type: ${target.toUpperCase()}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // ADD WHITELIST
    if (action === 'add') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        const whitelist = await db.get('antistickerWhitelist') || []
        if (!whitelist.includes(userJid)) {
          whitelist.push(userJid)
          await db.set('antistickerWhitelist', whitelist)
        }
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вњ… User Whitelisted\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // DEL / REMOVE
    if (action === 'del' || action === 'remove' || action === 'delete') {
      if (target === 'whitelist' || target === 'wl') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        let whitelist = await db.get('antistickerWhitelist') || []
        whitelist = whitelist.filter(u => u!== userJid)
        await db.set('antistickerWhitelist', whitelist)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ рџ—‘пёЏ User Removed\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
        }, { quoted: m })
      }
    }

    // CLEAR ALL
    if (action === 'clear' || action === 'reset') {
      await db.set('antistickerWhitelist', [])
      await db.set('antistickerGroups', [])
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ рџ—‘пёЏ All Lists Cleared\nв•‘ Whitelist + Groups reset\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
      }, { quoted: m })
    }

    // INVALID
    await sock.sendMessage(from, {
      text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’\nв•‘ вқЊ Invalid command\nв•‘ Use: ${prefix}ast status\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвқ’`
    }, { quoted: m })
  }
}