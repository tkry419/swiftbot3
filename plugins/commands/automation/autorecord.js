/**
 * SwiftBot - plugins/commands/automation/autorecord.js
 * Auto Recording Manager - Full Control
 * Set time, targets, mode - Owner only
 */

export default {
  name: 'autorecord',
  alias: ['arecord', 'autorec', 'recording', 'ar'],
  desc: 'Control auto recording presence globally or per target',
  usage: '[on/off/status] [global/dm/groups/user] [time]',
  category: 'Automation',
  permission: 'owner',

  execute: async (sock, m, args, { db, prefix, isOwner }) => {
    const from = m.key.remoteJid
    const isGroup = from.endsWith('@g.us')

    if (!isOwner) {
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Owner only command\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    const action = args[0]?.toLowerCase()
    const target = args[1]?.toLowerCase()
    const value = args[2]

    // STATUS
    if (!action || action === 'status' || action === 'info') {
      const [
        record, delay,
        groups, users, dmEnabled, groupsEnabled
      ] = await Promise.all([
        db.get('autrecord'),
        db.get('autrecordDelay'),
        db.get('autrecordGroups'),
        db.get('autrecordUsers'),
        db.get('autrecordDM'),
        db.get('autrecordGroupsEnabled')
      ])

      const groupList = groups?.length? groups.map(g => g.split('@')[0]).join(', ') : 'All'
      const userList = users?.length? users.map(u => u.split('@')[0]).join(', ') : 'All'

      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’
в•‘ рџЋ™пёЏ AUTO RECORD STATUS
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ Recording: ${record? 'рџџў ON' : 'рџ”ґ OFF'}
в•‘ Delay: ${delay || 2000}ms
в•‘
в•‘ рџ“Ќ TARGETS:
в•‘ DM Global: ${dmEnabled!== false? 'вњ… ON' : 'вќЊ OFF'}
в•‘ Groups Global: ${groupsEnabled!== false? 'вњ… ON' : 'вќЊ OFF'}
в•‘ Specific Groups: ${groups?.length || 0}
в•‘ ${groups?.length? 'р– Ѓ ' + groupList : ''}
в•‘ Specific Users: ${users?.length || 0}
в•‘ ${users?.length? 'р– Ѓ ' + userList : ''}
в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
в•‘ рџ“ќ USAGE:
в•‘ ${prefix}ar on global
в•‘ ${prefix}ar on dm
в•‘ ${prefix}ar on groups
в•‘ ${prefix}ar on user 255712345678
в•‘ ${prefix}ar on group (this group)
в•‘ ${prefix}ar off global
в•‘ ${prefix}ar time 4000
в•‘ ${prefix}ar add user 255xxx
в•‘ ${prefix}ar del group
в•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    // ON / OFF
    if (action === 'on' || action === 'enable') {
      if (!target || target === 'global') {
        await db.set('autrecord', true)
        await db.set('auttyping', false)
        await db.set('autrecordDM', true)
        await db.set('autrecordGroupsEnabled', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Auto Recording Enabled\nв•‘ Mode: GLOBAL\nв•‘ Target: All DMs + All Groups\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'dm' || target === 'dms' || target === 'private') {
        await db.set('autrecord', true)
        await db.set('autrecordDM', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Auto Recording Enabled\nв•‘ Mode: DMs ONLY\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'groups' || target === 'group' || target === 'gc') {
        await db.set('autrecord', true)
        await db.set('autrecordGroupsEnabled', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Auto Recording Enabled\nв•‘ Mode: GROUPS ONLY\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'user') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        const users = await db.get('autrecordUsers') || []
        if (!users.includes(userJid)) {
          users.push(userJid)
          await db.set('autrecordUsers', users)
        }
        await db.set('autrecord', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Auto Recording Enabled\nв•‘ Target: ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        const groups = await db.get('autrecordGroups') || []
        if (!groups.includes(from)) {
          groups.push(from)
          await db.set('autrecordGroups', groups)
        }
        await db.set('autrecord', true)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Auto Recording Enabled\nв•‘ Target: THIS GROUP\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // OFF / DISABLE
    if (action === 'off' || action === 'disable') {
      if (!target || target === 'global') {
        await db.set('autrecord', false)
        await sock.sendPresenceUpdate('paused', from)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Auto Recording Disabled\nв•‘ Mode: GLOBAL OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'dm' || target === 'dms') {
        await db.set('autrecordDM', false)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Auto Recording Disabled\nв•‘ Mode: DMs OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'groups' || target === 'group') {
        await db.set('autrecordGroupsEnabled', false)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Auto Recording Disabled\nв•‘ Mode: GROUPS OFF\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // TIME / DELAY
    if (action === 'time' || action === 'delay') {
      const newDelay = parseInt(target)
      if (isNaN(newDelay) || newDelay < 500 || newDelay > 10000) {
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Invalid delay\nв•‘ Range: 500-10000ms\nв•‘ Example: ${prefix}ar time 4000\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
      await db.set('autrecordDelay', newDelay)
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вЏ±пёЏ Recording Delay Set\nв•‘ Duration: ${newDelay}ms\nв•‘ ${(newDelay/1000).toFixed(1)} seconds\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    // ADD
    if (action === 'add') {
      if (target === 'user') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        const users = await db.get('autrecordUsers') || []
        if (!users.includes(userJid)) {
          users.push(userJid)
          await db.set('autrecordUsers', users)
        }
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… User Added\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        const groups = await db.get('autrecordGroups') || []
        if (!groups.includes(from)) {
          groups.push(from)
          await db.set('autrecordGroups', groups)
        }
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вњ… Group Added\nв•‘ This group whitelisted\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // DEL / REMOVE
    if (action === 'del' || action === 'remove' || action === 'delete') {
      if (target === 'user') {
        const userJid = value?.includes('@')? value : `${value}@s.whatsapp.net`
        let users = await db.get('autrecordUsers') || []
        users = users.filter(u => u!== userJid)
        await db.set('autrecordUsers', users)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ рџ—‘пёЏ User Removed\nв•‘ ${value}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }

      if (target === 'group' && isGroup) {
        let groups = await db.get('autrecordGroups') || []
        groups = groups.filter(g => g!== from)
        await db.set('autrecordGroups', groups)
        return await sock.sendMessage(from, {
          text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ рџ—‘пёЏ Group Removed\nв•‘ This group removed\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        }, { quoted: m })
      }
    }

    // CLEAR ALL
    if (action === 'clear' || action === 'reset') {
      await db.set('autrecordUsers', [])
      await db.set('autrecordGroups', [])
      return await sock.sendMessage(from, {
        text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ рџ—‘пёЏ All Targets Cleared\nв•‘ Whitelist reset\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      }, { quoted: m })
    }

    // INVALID
    await sock.sendMessage(from, {
      text: `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ вќЊ Invalid command\nв•‘ Use: ${prefix}ar status\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
    }, { quoted: m })
  }
}