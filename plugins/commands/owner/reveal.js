/**
 * SwiftBot - plugins/commands/owner/cleardb.js
 * Clear Database - Reset All Settings to Defaults
 * Owner only, requires confirmation
 */

function getName(msg, jid) {
  return msg.pushName || jid.split('@')[0]
}

import { DEFAULTS } from '../../system/db.js'

export default {
  name: 'cleardb',
  alias: ['resetdb', 'factoryreset', 'wipedb'],
  desc: 'Reset entire database to defaults',
  usage: '<confirm>',
  category: 'owner',
  permission: 'owner',

  execute: async (sock, m, args, { db, box, nobox }) => {
    const from = m.key.remoteJid
    const sender = m.key.participant || m.key.remoteJid
    const senderName = getName(m, sender)

    const confirm = args[0]?.toLowerCase()

    if (confirm!== 'confirm') {
      const msg = nobox
   ? `вљ пёЏ DANGER: Clear Database\n\nThis will RESET ALL settings to defaults:\nвЂў All configs wiped\nвЂў All groups/users data deleted\nвЂў All stats reset\nвЂў Cannot be undone\n\nTo proceed: #cleardb confirm`
        : `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ *вљ пёЏ DANGER: CLEAR DB*\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ This will RESET ALL:\nв•‘\nв•‘ вЂў All settings в†’ defaults\nв•‘ вЂў All group data deleted\nв•‘ вЂў All user data deleted\nв•‘ вЂў All stats reset to 0\nв•‘\nв•‘ CANNOT BE UNDONE!\nв•‘\nв•‘ To proceed:\nв•‘ #cleardb confirm\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
      return await sock.sendMessage(from, { text: msg }, { quoted: m })
    }

    const sent = await sock.sendMessage(from, {
      text: nobox
   ? `Resetting database...\n\nBy: ${senderName}\n\nPlease wait...`
        : `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ *CLEAR DATABASE*\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ Owner: ${senderName}\nв•‘\nв•‘ Resetting all data...\nв•‘ Please wait...\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
    }, { quoted: m })

    try {
      // Get all current keys
      const allData = await db.getAll()
      const keys = Object.keys(allData)

      // Delete all existing keys
      for (const key of keys) {
        await db.delete(key)
      }

      // Restore defaults
      for (const [key, value] of Object.entries(DEFAULTS)) {
        await db.set(key, value)
      }

      // If MongoDB, also clear groups and users collections
      if (db.mode === 'mongodb') {
        await db._settings.deleteMany({ key: { $nin: Object.keys(DEFAULTS) } })
        // Note: Groups/Users clearing needs direct collection access
        // This clears main settings only
      }

      try {
        await sock.sendMessage(from, {
          edit: sent.key,
          text: nobox
       ? `Database cleared вњ…\n\nAll settings reset to defaults\nBy: ${senderName}\n\nRestart recommended: #restart`
            : `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ *DATABASE CLEARED*\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ Status: Success вњ…\nв•‘ All settings в†’ defaults\nв•‘ By: ${senderName}\nв•‘\nв•‘ Restart recommended\nв•‘ Use: #restart\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        })
      } catch {}

    } catch (error) {
      try {
        await sock.sendMessage(from, {
          edit: sent.key,
          text: nobox
       ? `Failed to clear DB\n\nError: ${error.message}`
            : `в•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ *CLEAR DB FAILED*\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•”в•ђв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’\nв•‘ Error: ${error.message}\nв•љв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв•ђвќ’`
        })
      } catch {}
    }
  }
}